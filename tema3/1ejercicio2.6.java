public class UsoDeOperadoresAritmeticos {
 public static void main(String[] args) {
   int x = 144;
   int y = 999;
   int suma = x + y;
   System.out.println("La suma entre x e y es: "+suma);
   int div = x / y;
   System.out.println("La division entre x e y es: "+div);
   int res = x - y;
   System.out.println("La resta entre x e y es: "+res);
   int mul = x * y;
   System.out.println("La multiplicacion entre x e y es: "+mul);
  
  }
}
